package org.tehlab.whitek0t.fictionbook.internal.sanitizer;

import org.tehlab.whitek0t.fictionbook.dto.FictionBookDto;

/**
 * Санитайзер — преобразует {@link FictionBookDto} для приведения к стандарту.
 *
 * <p>Используется перед записью книги в файл для:</p>
 * <ul>
 *   <li>Удаления пустых/мусорных узлов</li>
 *   <li>Склейки разбитых текстовых нод</li>
 *   <li>Проверки валидности внутренних ссылок</li>
 *   <li>Нормализации атрибутов</li>
 * </ul>
 *
 * <p><b>Принцип:</b> "прощающее чтение, строгая запись".
 * При чтении допускаются любые отклонения от стандарта, но при записи
 * книга приводится к строгому соответствию спецификации.</p>
 *
 * <p><b>Immutable:</b> санитайзер возвращает новую копию DTO,
 * не модифицируя исходную.</p>
 *
 * <p><b>Идемпотентность:</b> повторный запуск санитайзера не должен
 * вносить дополнительных изменений.</p>
 */
@FunctionalInterface
public interface Sanitizer {

    /**
     * Применяет санитизацию к книге.
     *
     * @param book исходная книга (immutable)
     * @return новая книга с применёнными изменениями
     */
    FictionBookDto sanitize(FictionBookDto book);
}
